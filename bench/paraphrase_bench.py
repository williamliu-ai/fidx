"""Paraphrase query benchmark — the semantic-recall follow-up.

Motivation (independent reviewer feedback on the v0.1.0 benchmark): the
known-item queries are built from tokens taken from the target document, so
they reward lexical overlap and under-test semantic paraphrase. This harness
measures semantic recall directly:

  gen      — for EVERY existing known-item query, an LLM writes a paraphrase
             query for the same target document: natural wording, with the
             document's distinctive terms (names, identifiers, rare words)
             deliberately paraphrased away. Same count per corpus as the
             lexical-leaning sets. Resumable.
  validate — a separate LLM review of every generated query: is it specific
             enough that the target document is the best answer among
             thousands (retrievable), and does it avoid copying distinctive
             terms (no lexical leak)? Invalid queries are regenerated by a
             `gen --retry-invalid` pass; still-invalid ones are dropped and
             reported.
  replay   — runs the final queries against the KEPT benchmark indexes
             (no re-indexing): fidx in hybrid, lexical-only, and vector-only
             modes (the decomposition that answers "is the win lexical or
             semantic"), qmd `search` (BM25), and optionally qmd `query`
             (LLM hybrid; slow — gate with --qmd-query-limit).
  report   — recall@1/3/10 tables per corpus x engine/mode, side by side
             with the original vague-type known-item numbers, plus the
             query/document token-overlap distribution that shows the
             paraphrase set is actually non-lexical.

The LLM for gen/validate runs through the Codex CLI (`codex exec`), like
bench/judge_purity.py; generation and validation use separate calls so the
validator is not grading its own work in-context.

Run: .venv/bin/python bench/paraphrase_bench.py <gen|validate|replay|report|all>
     [--corpora docs,docs-small,chat,code] [--parallel 8] [--qmd-query-limit 0]
Artifacts: bench/data/queries-<corpus>-paraphrase.jsonl (final query sets),
           bench/results/paraphrase-{validation,lists}.jsonl, paraphrase-report.md
"""
from __future__ import annotations

import argparse
import collections
import json
import os
import re
import subprocess
import sys
import tempfile
import threading
from concurrent.futures import ThreadPoolExecutor
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))
import judge_purity as J
from run_bench import DATA, RESULTS
from gen_queries import STOPWORDS, TOKEN_RE

CORPORA_DEFAULT = ["docs-small", "docs", "chat", "code"]
FIDX_COMBO = ("e5-768", "sqlite-vec")   # same combo the published tables use
FIDX_MODES = ["hybrid", "lexical", "vector"]
VALIDATION = RESULTS / "paraphrase-validation.jsonl"
LISTS = RESULTS / "paraphrase-lists.jsonl"
REPORT = RESULTS / "paraphrase-report.md"
_LOCK = threading.Lock()

GEN_SCHEMA = {"type": "object", "additionalProperties": False,
              "required": ["query"], "properties": {"query": {"type": "string"}}}
VAL_SCHEMA = {"type": "object", "additionalProperties": False,
              "required": ["retrievable", "lexical_leak", "reason"],
              "properties": {"retrievable": {"type": "boolean"},
                             "lexical_leak": {"type": "boolean"},
                             "reason": {"type": "string"}}}

GEN_PROMPT = """\
You write ONE search query for a semantic-retrieval benchmark.

Read the document below. Imagine you read it days ago and now want to find it
again using a search engine that understands MEANING. Write the query you
would type: one natural line, 8–20 words, describing this document's
distinctive content in YOUR OWN words.

HARD RULES:
- Do NOT copy distinctive terms from the document: no proper names, no
  identifiers, no rare or technical words that appear in it. Paraphrase them
  (e.g. "the actress from the teen romance" instead of her name; "the
  function that retries failed network calls" instead of its identifier).
- Everyday common words are fine.
- The query must be SPECIFIC to this document — a searcher's best answer in a
  corpus of thousands of similar documents — not a generic topic query.
- Output JSON only: {{"query": "..."}}

DOCUMENT ({relpath}):
{body}
"""

VAL_PROMPT = """\
You are reviewing a query for a semantic-retrieval benchmark. The QUERY was
written to find the DOCUMENT without reusing its distinctive vocabulary.

Judge it:
- retrievable: is the query specific enough that this document would be the
  best answer among THOUSANDS of similar documents? (A generic topic query
  that matches hundreds of documents is NOT retrievable.)
- lexical_leak: does the query copy distinctive terms from the document
  (proper names, identifiers, rare/technical words)? Shared everyday words
  are not a leak.

Output JSON only: {{"retrievable": true/false, "lexical_leak": true/false,
"reason": "one line"}}

QUERY: {query}

DOCUMENT ({relpath}):
{body}
"""


def codex_json(prompt: str, schema: dict, timeout: int = 300) -> dict | None:
    env = {k: v for k, v in os.environ.items() if k != "ANTHROPIC_API_KEY"}
    for _ in (1, 2):
        with tempfile.TemporaryDirectory() as td:
            last, sch = Path(td) / "last.txt", Path(td) / "s.json"
            sch.write_text(json.dumps(schema))
            cmd = [J.CODEX_BIN, "exec", "-", "-C", str(J.REPO_ROOT),
                   "--dangerously-bypass-approvals-and-sandbox",
                   "--skip-git-repo-check", "--ephemeral",
                   "--output-schema", str(sch), "-o", str(last)]
            try:
                subprocess.run(cmd, input=prompt, env=env, capture_output=True,
                               text=True, timeout=timeout)
                t = last.read_text()
                return json.loads(t[t.index("{"): t.rindex("}") + 1])
            except (subprocess.TimeoutExpired, OSError, ValueError,
                    KeyError, json.JSONDecodeError):
                continue
    return None


def content_words(text: str) -> set[str]:
    return {t.lower() for t in TOKEN_RE.findall(text)} - STOPWORDS


def overlap_fraction(query: str, body: str) -> float:
    qw = content_words(query)
    if not qw:
        return 0.0
    dw = content_words(body)
    return sum(1 for w in qw if w in dw) / len(qw)


def para_path(corpus: str) -> Path:
    return DATA / f"queries-{corpus}-paraphrase.jsonl"


def load_source_queries(corpus: str) -> list[dict]:
    path = DATA / f"queries-{corpus}.jsonl"
    if not path.exists():
        sys.exit(f"{path} missing — the known-item set is the source of targets")
    return [json.loads(l) for l in path.read_text().splitlines() if l.strip()]


def gen_one(corpus: str, src: dict, attempt: int) -> dict | None:
    body = J.doc_text(corpus, src["expected"]) or ""
    if not body:
        return None
    extra = ""
    if attempt > 1:
        extra = ("\nPREVIOUS ATTEMPT WAS REJECTED (too generic or it copied "
                 "distinctive words). Be more specific about this document's "
                 "unique content and paraphrase harder.\n")
    resp = codex_json(GEN_PROMPT.format(relpath=src["expected"],
                                        body=body[:3500]) + extra, GEN_SCHEMA)
    if not resp or not resp.get("query", "").strip():
        return None
    q = " ".join(resp["query"].split())
    return {"qid": f"para-{src['qid']}", "type": "paraphrase", "query": q,
            "expected": src["expected"],
            "overlap": round(overlap_fraction(q, body), 3)}


def cmd_gen(args) -> None:
    invalid = set()
    if args.retry_invalid and VALIDATION.exists():
        for r in J.jsonl_read(VALIDATION):
            if not r["valid"]:
                invalid.add((r["corpus"], r["qid"]))
    for corpus in args.corpora:
        src = load_source_queries(corpus)
        out = para_path(corpus)
        have = {r["qid"]: r for r in J.jsonl_read(out)} if out.exists() else {}
        if args.retry_invalid:
            todo = [s for s in src if (corpus, f"para-{s['qid']}") in invalid]
            attempt = 2
        else:
            todo = [s for s in src if f"para-{s['qid']}" not in have]
            attempt = 1
        print(f"[{corpus}] generating {len(todo)}/{len(src)} paraphrase queries",
              file=sys.stderr)
        if not todo:
            continue
        done_n = 0
        def work(s: dict) -> None:
            nonlocal done_n
            row = gen_one(corpus, s, attempt)
            with _LOCK:
                done_n += 1
                if row:
                    have[row["qid"]] = row
                if done_n % 25 == 0:
                    print(f"  [{corpus}] {done_n}/{len(todo)}", file=sys.stderr)
        with ThreadPoolExecutor(max_workers=args.parallel) as ex:
            list(ex.map(work, todo))
        # rewrite the whole file in source order (stable, dedup, replaces retried)
        with out.open("w") as f:
            for s in src:
                row = have.get(f"para-{s['qid']}")
                if row:
                    f.write(json.dumps(row) + "\n")
        print(f"[{corpus}] {len(have)}/{len(src)} queries -> {out}", file=sys.stderr)


def cmd_validate(args) -> None:
    done = {(r["corpus"], r["qid"]) for r in J.jsonl_read(VALIDATION)} \
        if VALIDATION.exists() and not args.revalidate else set()
    if args.revalidate and VALIDATION.exists():
        VALIDATION.unlink()
        done = set()
    for corpus in args.corpora:
        rows = J.jsonl_read(para_path(corpus))
        todo = [r for r in rows if (corpus, r["qid"]) not in done]
        print(f"[{corpus}] validating {len(todo)}/{len(rows)}", file=sys.stderr)
        n = 0
        def work(r: dict) -> None:
            nonlocal n
            body = (J.doc_text(corpus, r["expected"]) or "")[:3500]
            resp = codex_json(VAL_PROMPT.format(query=r["query"],
                                                relpath=r["expected"], body=body),
                              VAL_SCHEMA)
            valid = bool(resp and resp["retrievable"] and not resp["lexical_leak"])
            with _LOCK:
                n += 1
                J.jsonl_append(VALIDATION, {
                    "corpus": corpus, "qid": r["qid"], "valid": valid,
                    "retrievable": bool(resp and resp.get("retrievable")),
                    "lexical_leak": bool(resp and resp.get("lexical_leak")),
                    "reason": (resp or {}).get("reason", "no verdict")})
                if n % 25 == 0:
                    print(f"  [{corpus}] {n}/{len(todo)}", file=sys.stderr)
        with ThreadPoolExecutor(max_workers=args.parallel) as ex:
            list(ex.map(work, todo))
    stats = collections.Counter()
    for r in J.jsonl_read(VALIDATION):
        stats[(r["corpus"], r["valid"])] += 1
    for corpus in args.corpora:
        ok, bad = stats[(corpus, True)], stats[(corpus, False)]
        print(f"[{corpus}] valid {ok} / invalid {bad}")


def final_queries(corpus: str) -> list[dict]:
    """Validated-only paraphrase queries for a corpus."""
    verdict = {r["qid"]: r["valid"] for r in J.jsonl_read(VALIDATION)
               if r["corpus"] == corpus}
    return [r for r in J.jsonl_read(para_path(corpus)) if verdict.get(r["qid"])]


def replay_fidx_modes(corpus: str, queries: list[dict], done: set) -> None:
    from fidx import daemon as fidx_daemon
    profile, backend = FIDX_COMBO
    db = RESULTS / f"fidx-{profile}-{backend}-{corpus}.db"
    if not db.exists():
        print(f"SKIP fidx {corpus}: no index at {db}", file=sys.stderr)
        return
    todo = [(q, m) for q in queries for m in FIDX_MODES
            if (corpus, "fidx", m, q["qid"]) not in done]
    if not todo:
        return
    env = os.environ.copy()
    env["FIDX_THREADS"] = "1"
    sock = fidx_daemon.socket_path_for(db)
    sock.unlink(missing_ok=True)
    server = subprocess.Popen(
        [sys.executable, "-m", "fidx.cli", "--db", str(db), "serve"],
        env=env, stderr=subprocess.DEVNULL)
    try:
        import time
        deadline = time.time() + 120
        while time.time() < deadline:
            if sock.exists() and fidx_daemon.client_request(db, {"cmd": "ping"}):
                break
            time.sleep(0.2)
        hmap = J.unhandelize_map(corpus)
        for i, (q, mode) in enumerate(todo, 1):
            resp = fidx_daemon.client_request(
                db, {"cmd": "search", "query": q["query"], "mode": mode, "limit": 10})
            if not resp or not resp.get("ok"):
                continue
            paths = [J.real_path(r["path"], hmap, corpus) or r["path"]
                     for r in resp["results"]]
            srcs = [r.get("sources") for r in resp["results"]]
            J.jsonl_append(LISTS, {"corpus": corpus, "engine": "fidx",
                                   "mode": mode, "qid": q["qid"],
                                   "query": q["query"], "expected": q["expected"],
                                   "paths": paths, "sources": srcs})
            if i % 100 == 0:
                print(f"  fidx {corpus}: {i}/{len(todo)}", file=sys.stderr)
    finally:
        server.terminate()
        try:
            server.wait(timeout=10)
        except subprocess.TimeoutExpired:
            server.kill()
    print(f"replayed fidx {corpus}: {len(todo)} (query,mode) pairs", file=sys.stderr)


def cmd_replay(args) -> None:
    done = {(r["corpus"], r["engine"], r["mode"], r["qid"])
            for r in J.jsonl_read(LISTS)} if LISTS.exists() else set()
    for corpus in args.corpora:
        qs = final_queries(corpus)
        if not qs:
            print(f"SKIP {corpus}: no validated queries (run gen+validate)",
                  file=sys.stderr)
            continue
        print(f"[{corpus}] replaying {len(qs)} validated paraphrase queries",
              file=sys.stderr)
        replay_fidx_modes(corpus, qs, done)
        J.replay_qmd(corpus, "search", qs, done)
        if args.qmd_query_limit:
            J.replay_qmd(corpus, "query", qs[:args.qmd_query_limit], done)


def _recall(rows: list[dict]) -> dict:
    n = len(rows)
    hits = {1: 0, 3: 0, 10: 0}
    for r in rows:
        rank = next((i for i, p in enumerate(r["paths"])
                     if J.is_expected(p, r["expected"])), None)
        for k in hits:
            if rank is not None and rank < k:
                hits[k] += 1
    return {"n": n, **{f"r@{k}": round(v / n, 3) if n else 0.0
                       for k, v in hits.items()}}


def cmd_report(args) -> None:
    lists = J.jsonl_read(LISTS)
    by = collections.defaultdict(list)
    for r in lists:
        by[(r["corpus"], r["engine"], r["mode"])].append(r)
    out = ["# Paraphrase-query benchmark (semantic recall)", ""]
    for corpus in args.corpora:
        qs = final_queries(corpus)
        if not qs:
            continue
        ov = [q["overlap"] for q in qs]
        ov_mean = sum(ov) / len(ov)
        out += [f"## {corpus} — {len(qs)} validated paraphrase queries",
                f"query→doc content-word overlap: mean {ov_mean:.2f} "
                f"(≤0.2: {sum(1 for x in ov if x <= .2)}, "
                f">0.5: {sum(1 for x in ov if x > .5)}); "
                "the original known-item sets are ~1.0 by construction.", "",
                "| engine / mode | n | R@1 | R@3 | R@10 |", "|---|---|---|---|---|"]
        for (c, engine, mode), rows in sorted(by.items()):
            if c != corpus:
                continue
            m = _recall(rows)
            out.append(f"| {engine} {mode} | {m['n']} | {m['r@1']} | "
                       f"{m['r@3']} | {m['r@10']} |")
        # hybrid source attribution: which arm carried the hits
        hyb = by.get((corpus, "fidx", "hybrid"), [])
        vec_only = lex_only = both = 0
        for r in hyb:
            for p, s in zip(r["paths"], r.get("sources") or []):
                if J.is_expected(p, r["expected"]) and isinstance(s, dict):
                    has_v, has_l = "vector" in s, "lexical" in s
                    vec_only += has_v and not has_l
                    lex_only += has_l and not has_v
                    both += has_v and has_l
                    break
        tot = vec_only + lex_only + both
        if tot:
            out += ["", f"hybrid hits@10 carried by: vector-only {vec_only} "
                    f"({vec_only/tot:.0%}), lexical-only {lex_only} "
                    f"({lex_only/tot:.0%}), both arms {both} ({both/tot:.0%})", ""]
        out.append("")
    report = "\n".join(out)
    REPORT.write_text(report)
    print(report)


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("cmd", choices=["gen", "validate", "replay", "report", "all"])
    p.add_argument("--corpora", default=",".join(CORPORA_DEFAULT))
    p.add_argument("--parallel", type=int, default=8)
    p.add_argument("--retry-invalid", action="store_true",
                   help="gen: regenerate only queries that failed validation")
    p.add_argument("--revalidate", action="store_true",
                   help="validate: wipe verdicts and re-review everything")
    p.add_argument("--qmd-query-limit", type=int, default=0,
                   help="also run qmd `query` (LLM) on the first N queries")
    args = p.parse_args()
    args.corpora = [c.strip() for c in args.corpora.split(",") if c.strip()]
    if args.cmd in ("gen", "all"):
        cmd_gen(args)
    if args.cmd in ("validate", "all"):
        cmd_validate(args)
    if args.cmd in ("replay", "all"):
        cmd_replay(args)
    if args.cmd in ("report", "all"):
        cmd_report(args)


if __name__ == "__main__":
    main()
